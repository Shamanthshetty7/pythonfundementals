# 3. Find X raised to power N

# You are given two integers: X and N. You have to calculate X raised to power N and print it.

# Sample Input:
# 10
# 4

# Sample Output:
# 10000

# So. here is the code

x=int(input())   # for input taking from users       
n=int(input())
s=x**n           # for finding power of something we can use (base value)**(exponential value)  # ** is used for taking exponent
print(s)  

